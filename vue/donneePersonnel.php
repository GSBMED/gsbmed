Pourquoi transférons-nous vos données ?

Le transfert de vos données en dehors de l'Union Européenne s’explique principalement par la localisation de nos sous-traitants.
En effet, afin de vous proposer un service de qualité, nous avons décidé d'externaliser certaines opérations auprès de prestataires spécialisés qui disposent d'une réelle expertise dans leurs domaines (Un exemple ? L’hébergement). Et certains de ces prestataires sont établis en dehors de l’Union Européenne.
<br>Comment sont encadrés ces transferts de données ?
Quels que soient les pays où sont établis nos sous-traitants, nous les sélectionnons toujours avec la plus grande vigilance. C’est tout particulièrement le cas en ce qui concerne la manière dont sont sécurisées vos données. Ces sous-traitants n’agissent que sur instruction de Gsbmed et n'utilisent à aucun moment vos données pour autre chose que l’exécution des prestations que nous leur confions. 
Un contrat est systématiquement conclu entre Gsbmed et les sous-traitants sélectionnés.
Lorsque que nos prestataires sont établis en dehors de l’Union Européenne, nous avons à coeur de les sélectionner en priorité dans des pays dont le cadre légal apporte un niveau de sécurité satisfaisant.
La liste de ces pays est disponible ici :
https://www.cnil.fr/fr/la-protection-des-donnees-dans-le-monde
