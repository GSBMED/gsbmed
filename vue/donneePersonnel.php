      <div class="accordion-body">Le fait de nous confier vos données ne signifie pas que vous perdez la maîtrise de celles-ci, bien au contraire ! Un peu d’explication...

Vos disposez de plusieurs droits. Certains sont applicables à tous les traitements de données vous concernant, d’autres dépendent du type de traitement mis en oeuvre et , plus précisément, du fondement juridique sur lequel il repose (contrat, obligation légale, consentement, intérêt légitime, etc .).

Vous trouverez ci-dessous un bref descriptif de ces droits ainsi que les modalités de leur mise en oeuvre.

Pour connaître précisément les droits assortis aux traitements vous concernant, reportez vous à la rubrique « vos droits » présentes dans la description du traitement concerné.
<br>
<strong>Le droit d’accès :</strong>

Ce droit vous permet de nous poser des questions sur la nature des traitements vous concernant (type de données, origine de la collecte, etc.). Il vous permet également de nous demander une copie de l’ensemble des informations vous concernant. Ce droit s’applique quel que soit le fondement juridique du traitement vous concernant.
<br>
<strong>Le droit de rectification :</strong>

Déménagement, mariage, nouveau sport, ... nombreuses sont les situations pouvant vous conduire à mettre à jour vos données. Le droit de rectification sert précisément à ça. Ce droit s’applique quel que soit le fondement juridique du traitement vous concernant.
<br>
<strong>Le droit d’opposition :</strong>

C’est le droit de dire «non !» , le droit de ne pas figurer dans un traitement de données ou de de ne plus y figurer. Ce droit s’applique lorsque le traitement repose sur « l’intérêt légitime » de Decathlon, pourvu que cet intérêt ne soit pas «impérieux ».
<br>
<strong>Droit à l’effacement :</strong>

C'est le droit d'être oublié... et c'est bien normal. Decathlon n'a aucune raison de conserver vos données au delà de ce qui est nécessaire. Toutes les données vous concernant sont donc automatiquement supprimées à l'expiration du délai de conservation annoncé lors de la collecte de vos données.
Lorsque vos données font l'objet d'un traitement reposant sur votre consentement ou sur l'intérêt légitime (non impérieux) de Decathlon, vous pouvez obtenir la suppression anticipée de vos données. Comment? il vous suffit de nous de nous manifester votre volonté de procéder au retrait de votre consentement ou de faire valoir votre droit d'opposition au traitement.
N'oubliez pas, en vous connectant à votre compte Decathlon, vous pouvez le supprimer (ainsi que les données y étant associées) par vous-même : Cliquez ici
<br>
<strong>Droit à la portabilité : </strong>

Ce droit vous permet de demander l'obtention des données vous concernant, dès lors que celles-ci sont font l'objet d'un traitement reposant sur votre consentement ou une relation contractuelle. Quelle différence avec le droit d'accès? Nous avons l'obligation de vous transmettre les données dans un format techniquement exploitable par vous même ou par une autre entité que Decathlon.
<br>
<strong>Droit à la limitation :</strong>

Ce droit est un droit complémentaire à certains des autres droits exposés ci-avant. En fait il vous permet de faire " figer " vos données après une demande de rectification par exemple. En cas de doute sur la légalité de l'un de nos traitements, ce droit vous permettrait de demander à nos équipes ne plus traiter vos données, mais cependant sans les effacer. Enfin, si vous souhaitez faire constater, exercer ou défendre vos droits en justice, vous pourriez également demander grâce à ce droit, de faire "geler" vos données personnelles. Pour toutes questions, n'hésitez pas à nous contacter !
